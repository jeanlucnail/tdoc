L_DIAG_UNPINNED_DESC="Dependensi tanpa versi yang di-pin akan menginstall versi terbaru saat itu — berpotensi merusak project pada install berikutnya."
L_DIAG_UNPINNED_CAUSE1="requirements.txt menggunakan nama paket tanpa versi (misal 'requests' bukan 'requests==2.31.0')"
